# task 2: take 2 user-input and print quotient and reminder
a = int(input("enter number 1"))
b = int(input("enter number 2"))
sum = a + b
sub = a - b
mul = a * b
div = a / b
print("sum is is :", sum)
print("sub is :", sub)
print("mul is :", mul)
print("div is :", div)
# Quotient
q = a // b  # double division operator
print("Quotient:", q)
# remainder
r = a % b
print("remainder:", r)
